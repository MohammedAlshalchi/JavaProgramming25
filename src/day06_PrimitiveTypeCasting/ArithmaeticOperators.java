package day06_PrimitiveTypeCasting;

public class ArithmaeticOperators {
    public static void main(String[] args) {
        System.out.println(10+20); // Addition
        // System.out.println("12"+1); will print: 121
        System.out.println("-----------------------------");
        System.out.println(110-50); // subtraction
        System.out.println("--------------------------------------");
        // in math 10/4=2.5
        // in java 10/4=2
        // in java 10.0/4=2.5
        System.out.println(100/3);
        System.out.println(100.0/3);
        System.out.println(100/3.0);
        System.out.println(100d/3); // we can put (d or D)
        System.out.println("-------------------------------------");
        int a = 100;
         double b = a/6; // 16.0 because the double
        System.out.println(b);
        System.out.println("----------");














    }















}
