package day06_PrimitiveTypeCasting;

public class BirthDay {
    public static void main(String[] args) {
     String name = " Mohammed";
     int birthDay = 06;
     String birthDyMonth = "June";
     int BirthYear = 1978;
        System.out.println(name+" was born on "+birthDyMonth+" // "+birthDay+" // "+BirthYear);
        System.out.println("-------------------------------------------------------");
        System.out.println(name+" was born on "+birthDyMonth+" /"+birthDay+"/"+BirthYear);
        System.out.println("-------------------------------------------------------");
        System.out.println(name+" was born on "+birthDyMonth+'/'+birthDay+'/'+BirthYear);
        System.out.println("-------------------------------------------------------");
        System.out.println(name+" was born on "+birthDyMonth+'/'+birthDay+'/'+BirthYear);// we can use char
        System.out.println("-------------------------------------------------------");
// My favorite book is "bookName"
        String bookName = "The rich dad and the poor dad";
        System.out.println("My favorite book is"+bookName);
        System.out.println("-------------------------------------------------------");
        System.out.println("My favorite book is \""+bookName+"\"");
        System.out.println("-------------------------------------------------------");
        System.out.println("My favorite book is "+" \""+bookName+"\"");// other way
        System.out.println("-------------------------------------------------------");
        /*
        \n  // we have to use back slash
        \t
        \'
        \"
        \\
         */
        System.out.println("-------------------------------------------------------");
        System.out.println("My favorite book is "+" \'"+bookName+"\'"); // we use ' '








    }










}
