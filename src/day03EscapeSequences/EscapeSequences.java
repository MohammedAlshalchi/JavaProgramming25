package day03EscapeSequences;
/*
escape sequences: MUST be given with double quote
\n: starts new line
\t: paragraph space (tab)
\\: one back slash (\)
\*: double qout
 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \nPaython \nC#");
        System.out.println("-------------------");
        System.out.println("Hello Cydeo \nHow are you all today ? \nIt's nice to see you all ! \nwhat class do we have next week");
        System.out.println("-----------------------------");
        System.out.println("\tJava is cool language ");
        System.out.println("/ \\"); // in order to print one back word we need tow print backward slashes
        System.out.println(" My favorite    TV show \" game of throne \" ");
        System.out.println("-------------------");
        System.out.println(" My favorite TV show \" game of throne \" ");



    }


}
